def generate_unsupervised_masks(encoder, X):
    _, _, z = encoder.predict(X)
    segments = []
    for i in range(len(X)):
        latent_map = np.abs(z[i]).reshape(8, 4)
        mask_res = cv2.resize(latent_map, (IMG_SIZE, IMG_SIZE))
        mask_norm = (mask_res - mask_res.min()) / (mask_res.max() - mask_res.min() + 1e-7)
        segments.append((mask_norm > 0.5).astype(np.uint8))
    return np.array(segments)

unsupervised_masks = generate_unsupervised_masks(encoder, X_train)

